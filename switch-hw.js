//Nested If-Else 3
let trafficLight = 'green';
switch(trafficLight) {
    case 'red':
        console.log('Stop');
        break;
    case 'yellow':
        console.log('Slow down');
        break;
    case 'green':
        console.log('Go');
        break;
    default:
        console.log('Proceed with caution');
}

